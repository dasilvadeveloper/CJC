/*
Var, short for variable, is a JavaScript keyword that creates, or declares, a new variable.

Camelcase is standard capitalization convention in JavaScript.

Rules for naming variables:
- Variable names cannot start with numbers.
- Variable names are case sensitive, so myName and myname would be different variables. It is bad practice to create two variables that have the same name using different cases.
- Variable names cannot be the same as keywords. All keywords are listed in MDN's keyword documentation: https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Lexical_grammar#keywords
*/
var favoriteFood = 'pizza';
var numOfSlices = 8;
