/*
There are built-in objects in JavaScript, and developers can also build their own objects

One example of a built-in object is the Math object

Objects have methods available to be used

The Math.floor() method takes a decimal number, and rounds down to the nearest whole number.
*/
console.log(Math.random() * 100);
console.log(Math.floor(Math.random() * 100));
console.log(Math.ceil(43.8));
// number object is used instead of math object
