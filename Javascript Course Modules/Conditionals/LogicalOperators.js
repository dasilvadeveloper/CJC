let mood = 'sleepy';
let tirednessLevel = 6;
/*
Logical operators in JavaScript:
the "and" operator (&&)
the "or" operator (||)
the "not" operator, otherwise known as the bang operator (!)
*/
if (mood === 'sleepy' || tirednessLevel > 8) {
  console.log('time to sleep');
}
else {
  console.log('not bed time yet');
}
